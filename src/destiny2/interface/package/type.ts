const withinRange = (type: RawFileType, floor: RawFileType, ceiling: RawFileType): boolean => type >= floor && type <= ceiling;

export function RawFileTypeName(type: RawFileType) { return RawFileType[type]; }

export function ContentTypeName(type: ContentType) { return ContentType[type]; }

export function RawFileTypeToContentType(type: RawFileType): ContentType {

  // Handle un-ranged raw types first
  switch (type) {
    case RawFileType.Unclassified:
      return ContentType.Unclassified;
    case RawFileType.ImageCombiner:
      return ContentType.ImageCombiner;
    case RawFileType.ImageCombinerReference:
      return ContentType.ImageCombinerReference;
    case RawFileType.Audio:
      return ContentType.Audio;
    case RawFileType.AudioReference:
      return ContentType.AudioReference;
    case RawFileType.AudioBlockHeader:
      return ContentType.AudioBlockHeader;
    case RawFileType.Text:
      return ContentType.Text;
    case RawFileType.TextReference:
      return ContentType.TextReference;
    case RawFileType.ModelMaster:
      return ContentType.ModelMaster;
    case RawFileType.DirectXShaderBytecodeChunk:
      return ContentType.DirectXShaderBytecodeChunk;
    case RawFileType.DirectXShaderBytecodeChunkReference:
      return ContentType.DirectXShaderBytecodeChunkReference;
    case RawFileType.HavokDataReference:
      return ContentType.HavokDataReference;
    case RawFileType.FilenameReference:
      return ContentType.FilenameReference;
    case RawFileType.Map:
      return ContentType.Map;
    case RawFileType.VoiceLines:
      return ContentType.VoiceLines;
    case RawFileType.Array:
      return ContentType.Array;
    case RawFileType.Entry:
      return ContentType.Entry;
    case RawFileType.Special:
      return ContentType.Special;
  }

  if (
    withinRange(type, RawFileType.Image00, RawFileType.ImageFF) ||
    withinRange(type, RawFileType.Image507D, RawFileType.Image6099)
  ) {
    return ContentType.Image;
  }

  if (withinRange(type, RawFileType.ImageReference00, RawFileType.ImageReferenceFF)) {
    return ContentType.ImageReference;
  }

  if (withinRange(type, RawFileType.ModelFaces8A, RawFileType.ModelFaces99)) {
    return ContentType.ModelFaces;
  }

  if (withinRange(type, RawFileType.ModelFaces8AReference, RawFileType.ModelFaces99Reference)) {
    return ContentType.ModelFacesReference;
  }

  if (withinRange(type, RawFileType.ModelVertices0A, RawFileType.ModelVertices19)) {
    return ContentType.ModelVertices;
  }

  if (withinRange(type, RawFileType.ModelVertices0AReference, RawFileType.ModelVertices19Reference)) {
    return ContentType.ModelVertices;
  }

  if (withinRange(type, RawFileType.ModelMiscellaneous94, RawFileType.ModelMiscellaneousDA)) {
    return ContentType.ModelMiscellaneous;
  }

  if (withinRange(type, RawFileType.ModelMiscellaneous94Reference, RawFileType.ModelMiscellaneousDAReference)) {
    return ContentType.ModelMiscellaneousReference;
  }

  if (withinRange(type, RawFileType.DirectXShaderBytecode0A, RawFileType.DirectXShaderBytecode9A)) {
    return ContentType.DirectXShaderBytecode;
  }

  if (withinRange(type, RawFileType.DirectXShaderBytecode0AReference, RawFileType.DirectXShaderBytecode9AReference)) {
    return ContentType.DirectXShaderBytecodeReference;
  }

  if (withinRange(type, RawFileType.HavokDataD4, RawFileType.HavokDataD9)) {
    return ContentType.HavokData;
  }

  if (withinRange(type, RawFileType.Filename816C, RawFileType.Filename941E)) {
    return ContentType.Filename;
  }

  if (withinRange(type, RawFileType.Undetermined426F, RawFileType.Undetermined9C54)) {
    return ContentType.Undetermined;
  }

  return ContentType.Unknown;
}
